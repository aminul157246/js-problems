// The sort() method sorts an array alphabetically:
const sortFruits = ["Banana", "Orange", "Apple", "Mango"];
console.log(sortFruits.sort())


// The reverse() method reverses the elements in an array:
const reverseFruits = ["Banana", "Orange", "Apple", "Mango"];
console.log(reverseFruits.reverse());


// toSorted  
